let celsius = parseFloat(prompt(`Digite a temperatura em graus celsius `)); // Essa variavel declara a temperatura em celsius

let fahrenheit = parseFloat (((celsius * 9) / 5)+32); // Essa variavel declara o calculo da temperatura de celsius para fahrenheit


console.log(`O valor da temperatura em Fahrenheit é: ` + fahrenheit); 

console.log(`O valor da temperatura em Fahrenheit é: ${fahrenheit.toFixed(2)}`); // Essa variavel declara a temperatura em fahrenheit


